    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        try {
            lerDadosTela();
            ClienteBO clienteBO = new ClienteBO();
            clienteBO.atualizarDados(cliente);
            this.dispose();
            exibirMensagemSucesso( "Dados alterados com sucesso!", "Alterar dados cliente");
        } catch (CamposVaziosException cve) {
            exibirMensagemErro(cve.getMessage(), "Alterar dados cliente");
        } catch (SQLException ex) {
            Logger.getLogger(EdicaoCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnSalvarActionPerformed

