    public void setValoresCampos() {
        txtNome.setText(cliente.getNome());
        txtRG.setText(cliente.getRG());
        txtTelefone.setText(cliente.getTelefone());
        txtEndereco.setText(cliente.getEndereco());
        if (cliente.isStatus()) {
            rbtPositivo.setSelected(true);
        } else {
            rbtNegativo.setSelected(true);
        }
    }
    
    public void lerDadosTela() {
        String rg = txtRG.getText().trim();
        String nome = txtNome.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String endereco = txtEndereco.getText().trim();

        boolean status;
        if (rbtPositivo.isSelected()) {
            status = true;
        } else {
            status = false;
        }

        String mensagem = "";

        if (rg.isEmpty()) {
            mensagem = "Campo RG é obrigatório.";
        }

        if (nome.isEmpty()) {
            mensagem = mensagem + "\nCampo nome é obrigatório.";
        }

        if (mensagem == "" || mensagem == null) {
            cliente.setRG(rg);
            cliente.setNome(nome);
            cliente.setTelefone(telefone);
            cliente.setEndereco(endereco);
            cliente.setStatus(status);
        } else {
            throw new CamposVaziosException(mensagem);
        }
    }

