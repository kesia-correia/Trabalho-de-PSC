    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try{
            lerDadosTela();
            ProdutoBO produtoBO = new ProdutoBO();
            produtoBO.atualizarDadosProduto(produto);
            telaConsultaProduto.buscarTodosProduto();
            exibirMensagemSucesso( "Produto atualizados com sucesso\n Codigo cadastrado : " + produto.getCodigo(), "Cadastrar produto");
        } catch(CamposVaziosException cve){
            exibirMensagemErro(cve.getMessage(), "Cadastrar produto");
        } catch(DescricaoDuplicadaProdutoException pdde){
            exibirMensagemErro(pdde.getMessage(),  "Cadastrar produto");
        }catch(QuantidadeProdutoInvalidaException qpie){
            exibirMensagemErro (qpie.getMessage(),  "Cadastrar produto");
        }catch(ValorProdutoInvalidoException vpie){
           exibirMensagemErro(vpie.getMessage(),  "Cadastrar produto");
        }catch(SQLException se){
            exibirMensagemErro("Erro desconhecido. Contate com o adminstrador do sistema",  "Cadastrar produto");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

