     private void lerDadosTela(){
        String descricao = txtDescricao.getText().trim();
        String nome = txtNome.getText().trim();
        String strQuantidadeEstoque = txtQuantidadeEstoque.getText().trim();
        String strValor = txtValor.getText().trim();
        String strQuantidadeMinima = txtQuantidadeMinima.getText().trim();
        
        String mensagem = "";
        
        if(descricao.isEmpty()){
            mensagem = "Campo descricao é obrigatório.";
        }
        
        int quantidade = 0;
        if(strQuantidadeEstoque.isEmpty()){
            mensagem = mensagem + "\nCampo Quantidade é obrigatório.";
        }else{
            try{
                quantidade = Integer.parseInt(strQuantidadeEstoque);
            } catch(NumberFormatException nfe){
                mensagem = mensagem + "\nQuantidade Invalida.";
            }
        }
        
        double valorUnitario = 0;
        if(strValor.isEmpty()){
            mensagem = mensagem + "\nCampo Valor Unitário é obrigatório.";
        }else{
            try {
                DecimalFormat formatador = new DecimalFormat("#,##0.00");
                valorUnitario = formatador.parse(strValor).doubleValue();
            } catch (ParseException ex) {
                mensagem = mensagem + "\nValor Unitário Invalido";
            }
        }
        
        int quantidadeMinima = 0;
        if(!strQuantidadeMinima.isEmpty()){
            try{
                quantidadeMinima = Integer.parseInt(strQuantidadeMinima);
            } catch(NumberFormatException nfe){
                mensagem = mensagem + "\nQuantidade Minima Invalida.";
            }
        }
        
        if(mensagem == "" || mensagem == null){
            produto.setDescricao(descricao);
            produto.setNome(nome);
            produto.setQuantidadeEstoque(quantidade);
            produto.setQuantidadeMinima(quantidadeMinima);
            produto.setValorUnitario(valorUnitario);
        }else{
            throw new CamposVaziosException(mensagem);
        }
    }
    
