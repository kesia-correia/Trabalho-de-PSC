    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        try{
        lerDados();
            UsuarioBO usuarioBO = new UsuarioBO();
            usuario = usuarioBO.login(login, senha);
            exibirMensagemSucesso("Usuario: " + usuario.getNome(), "Login usuário");
         MenuPrincipal principal = new MenuPrincipal(usuario);
         principal.setVisible(true);
         this.dispose();
        } catch(CamposVaziosException cve){
            exibirMensagemErro(cve.getMessage(), "Login usuário");
        } catch(InvalidoException ie){
            exibirMensagemErro(ie.getMessage(), "Login usuário");
        } catch (Exception e) {
            exibirMensagemErro("Erro desconhecido. Entre em contato com o adminstrador.", "Login usuário");
        } 
    }//GEN-LAST:event_btnLogarActionPerformed

