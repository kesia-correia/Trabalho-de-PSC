    private void lerDados() throws NoSuchAlgorithmException, UnsupportedEncodingException{
        String login = txtLogin.getText().trim();
        String senha = txtLogin.getText().trim();
        
        String mensagem = "";
        
        if(login.isEmpty()){
            mensagem = "O campo LOGIN é obrigatório.";
        }
        
        if(senha.isEmpty()){
            mensagem = mensagem + "O campo SENHA é obrigatório.";
        }
        
        if(mensagem == "" || mensagem == null){
            this.login = login;
            this.senha = Criptografia.exemploMD5(senha);
        }else{
            throw new CamposVaziosException(mensagem);
        }
    }
    
