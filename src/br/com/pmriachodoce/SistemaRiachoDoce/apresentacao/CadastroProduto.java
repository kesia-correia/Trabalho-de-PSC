    private String lerCNPJ() {
        String CNPJ = txtCNPJ.getText().trim();

        String mensagem = "";

        if (CNPJ.length() < 14) {
            mensagem = "CNPJ inválido.";
        }

        if (!(mensagem == "" || mensagem == null)) {
            throw new CamposVaziosException(mensagem);
        }

        return CNPJ;
    }
    
