    private void lerDadosTela(){
        String descricao = txtDescricao.getText().trim();
        String nome = txtNome.getText().trim();
        String strQuantidadeEstoque = txtQuantidadeEstoque.getText().trim();
        String strValor = txtValor.getText().trim();
        String strQuantidadeMinima = txtQuantidadeMinima.getText().trim();
        
        String mensagem = "";
        
        if(descricao.isEmpty()){
            mensagem = "Campo descricao é obrigatório.";
        }
        
        int quantidade = 0;
        if(strQuantidadeEstoque.isEmpty()){
            mensagem = mensagem + "\nCampo Quantidade é obrigatório.";
        }else{
            try{
                quantidade = Integer.parseInt(strQuantidadeEstoque);
            } catch(NumberFormatException nfe){
                mensagem = mensagem + "\nQuantidade Invalida.";
            }
        }
        
        double valorUnitario = 0;
        if(strValor.isEmpty()){
            mensagem = mensagem + "\nCampo Valor Unitário é obrigatório.";
        }else{
            try {
                DecimalFormat formatador = new DecimalFormat("#,##0.00");
                valorUnitario = formatador.parse(strValor).doubleValue();
            } catch (ParseException ex) {
                mensagem = mensagem + "\nValor Unitário Invalido";
            }
        }
        
        int quantidadeMinima = 0;
        if(!strQuantidadeMinima.isEmpty()){
            try{
                quantidadeMinima = Integer.parseInt(strQuantidadeMinima);
            } catch(NumberFormatException nfe){
                mensagem = mensagem + "\nQuantidade Minima Invalida.";
            }
        }
        
        if(mensagem == "" || mensagem == null){
            produto.setDescricao(descricao);
            produto.setNome(nome);
            produto.setQuantidadeEstoque(quantidade);
            produto.setQuantidadeMinima(quantidadeMinima);
            produto.setValorUnitario(valorUnitario);
        }else{
            throw new CamposVaziosException(mensagem);
        }
    }
    
    private void exibirMensagemErro(String mensagem, String titulo) {
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
        }

     private void exibirMensagemSucesso(String mensagem, String titulo) {
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        try{
            lerDadosTela();
            ProdutoBO produtoBO = new ProdutoBO();
            produtoBO.criar(produto);
            exibirMensagemSucesso( "Produto cadastro com sucesso\n Codigo cadastrado : " + produto.getCodigo(), "Cadastrar produto");
        } catch(CamposVaziosException cve){
            exibirMensagemErro(cve.getMessage(), "Cadastrar produto");
        } catch(DescricaoDuplicadaProdutoException pdde){
            exibirMensagemErro(pdde.getMessage(),  "Cadastrar produto");
        }catch(QuantidadeProdutoInvalidaException qpie){
            exibirMensagemErro (qpie.getMessage(),  "Cadastrar produto");
        }catch(ValorProdutoInvalidoException vpie){
           exibirMensagemErro(vpie.getMessage(),  "Cadastrar produto");
        }catch(SQLException se){
            exibirMensagemErro("Erro desconhecido. Contate com o adminstrador do sistema",  "Cadastrar produto");
        }
    }//GEN-LAST:event_BtnCadastrarActionPerformed

