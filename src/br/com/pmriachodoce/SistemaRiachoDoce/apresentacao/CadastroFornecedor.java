        private void exibirMensagemErro(String mensagem, String titulo) {
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
        }

        private void exibirMensagemSucesso(String mensagem, String titulo) {
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
        }

        private void lerDadosTela() throws ParseException {
            String CNPJ = txtCNPJ.getText().trim();
            String descricao = txtDescricao.getText().trim();
            String nome = txtNome.getText().trim();
            String telefone = txtTelefone.getText().trim();
            String endereco = txtEndereco.getText().trim();

            String mensagem = "";

            if(CNPJ.isEmpty()){
                mensagem = "Campo CNPJ é obrigatório.";
            }else if(CNPJ.length() < 14){
                mensagem = "CNPJ inválido.";
            }

            if(descricao.isEmpty()){
                mensagem = mensagem + "\nCampo Descrição é obrigatório.";
            }

            if(nome.isEmpty()){
                mensagem = mensagem + "\nCampo Nome é obrigatório.";
            }

            if(mensagem == "" || mensagem == null){
                fornecedor.setCNPJ(CNPJ);
                fornecedor.setDescricao(descricao);
                fornecedor.setNome(nome);
                fornecedor.setTelefone(telefone);
                fornecedor.setEndereco(endereco);
            }else{
                throw new CamposVaziosException(mensagem);
            }
    } 
    
