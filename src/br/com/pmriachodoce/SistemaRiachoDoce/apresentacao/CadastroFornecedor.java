    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        try {
            lerDadosTela();
            FornecedorBO fornecedorBO = new FornecedorBO();
            fornecedorBO.criar(fornecedor);
            exibirMensagemSucesso("Fornecedor Cadastrado com sucesso!", "Cadastro de Fornecedor");
        }catch(CamposVaziosException coe){
            exibirMensagemErro(coe.getMessage(), "Cadastrar Fornecedor");
        }catch(ParseException | SQLException e){
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de fornecedor", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnCadastrarActionPerformed

